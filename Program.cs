using Emdad_Dashboard.Data;
using Emdad_Dashboard.Helper;
using Microsoft.EntityFrameworkCore;

namespace Emdad_Dashboard
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.
            builder.Services.AddControllersWithViews();

            builder.Services.AddDbContext<ApplicationContext>(e =>
            {
                e.UseSqlServer(builder.Configuration.GetConnectionString("default"));
                //e.UseInMemoryDatabase("inmemory");
            });


            //services
            builder.Services.AddScoped<FileHelper>();


            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (app.Environment.IsDevelopment())
            {
                app.UseDeveloperExceptionPage();
            }
            else
            {
                app.UseExceptionHandler("/Home/Error");
                app.UseHsts();
            }


            app.UseHttpsRedirection();
            app.UseStaticFiles();

            app.UseRouting();

            app.UseAuthorization();

            app.MapControllerRoute(
                name: "default",
                pattern: "{controller=Home}/{action=Index}/{id?}");

            app.Run();
        }
    }
}
